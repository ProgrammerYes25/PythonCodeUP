#6006 : [기초-출력] 출력하기06(py)
#이번에는 특수문자 출력에 도전하자!!
#다음 문장을 출력하시오.
#"!@#$%^&*()'
#(단, 큰따옴표와 작은따옴표도 함께 출력한다.)
#출력 형식에 필요한 따옴표와 출력할 문자인 따옴표를 구분하기 위하여
